package com.bombadu.aprikot;

public class ListItem {
        private String authorText;
        private String descriptionText;
        private String imageUrlText;
        private int imageImage;

    public String getAuthorText() {
        return authorText;
    }

    public void setAuthorText(String authorText) {
        this.authorText = authorText;
    }

    public String getDescriptionText() {
        return descriptionText;
    }

    public void setDescriptionText(String descriptionText) {
        this.descriptionText = descriptionText;
    }

    public String getImageUrlText() {
        return imageUrlText;
    }

    public void setImageUrlText(String imageUrlText) {
        this.imageUrlText = imageUrlText;
    }

    public int getImageImage() {
        return imageImage;
    }

    public void setImageImage(int imageImage) {
        this.imageImage = imageImage;
    }
}
